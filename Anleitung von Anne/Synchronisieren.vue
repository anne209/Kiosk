Von local zu GitHub

Dafür sorgen, dass es remote gitHub auf dem Laptop gibt "kiosk [gitHub]"
File -> new window -> entweder open folder oder clone repository
programmieren und speichern
auf source control klicken
es gibt einen reiter "changes", unter "changes" sieht man die Veränderungen, die man vorgenommen hat
Die Veränderungen, die man committen will, auswählen und auf plus neben den Veränderungen klicken
Es öffnet sich staged changes, bei denen die changes drin sind, die committet werden sollen
bei message "commit" eingeben und auf "commit" drücken

bei kiosk [GitHub] auf sync klicken, dann synchronisieren
Alternativ auf Source Control klicken, dann auf Sync Changes (Pfeil nach unten)



